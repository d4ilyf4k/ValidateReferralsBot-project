@router.callback_query(
    F.data.in_({"black_classic", "black_aroma", "black_youth", "black_retro"}),
    StateFilter(BankAgreement.choosing_black_subtype)
)
async def choose_black_subtype(callback: types.CallbackQuery, state: FSMContext):
    mapping = {
        "black_classic": ("black_classic", "Tinkoff Black (Classic)"),
        "black_aroma": ("black_aroma", "–ê—Ä–æ–º–∞–∫–∞—Ä—Ç–∞ Black"),
        "black_youth": ("black_youth", "–ú–æ–ª–æ–¥—ë–∂–Ω–∞—è –∫–∞—Ä—Ç–∞ Black"),
        "black_retro": ("black_retro", "–†–µ—Ç—Ä–æ-–∫–∞—Ä—Ç–∞ Black")
    }
    
    if callback.data in mapping:
        product_key, product_name = mapping[callback.data]
        await state.update_data(
            product_key=product_key,
            product_name=product_name,
            black_subtype=callback.data
        )
        await show_final_conditions(callback, state, product_key, product_name)
    
    await callback.answer()


async def show_final_conditions(callback: types.CallbackQuery, state: FSMContext, product_key: str, product_name: str):
    text = _get_conditions_text(product_key, product_name)
    await state.set_state(BankAgreement.waiting_agreement)
    await callback.message.edit_text(text, parse_mode="HTML", reply_markup=get_agreement_kb())


@router.callback_query(F.data == "agree_conditions", StateFilter(BankAgreement.waiting_agreement))
async def process_agreement(callback: types.CallbackQuery, state: FSMContext):
    data = await state.get_data()
    user_id = data["user_id"]
    bank_key = data["bank_key"]
    product_key = data["product_key"]
    product_name = data["product_name"]

    await add_user_bank(user_id, bank_key, product_key, product_name)
    await recalculate_all_bonuses(user_id)

    link = await get_referral_link(bank_key, product_key)
    if not link:
        await callback.message.edit_text("‚ö†Ô∏è –°—Å—ã–ª–∫–∞ –≤—Ä–µ–º–µ–Ω–Ω–æ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞.")
        return

    await callback.message.edit_text(
        f"<b>üéâ –í–∞—à–∞ —Å—Å—ã–ª–∫–∞ –Ω–∞ {product_name}:</b>\n\n {link} \n\n"
        "<i>‚ö†Ô∏è –ù–µ –ø–µ—Ä–µ–¥–∞–≤–∞–π—Ç–µ –µ—ë –¥—Ä—É–≥–∏–º.</i>",
        parse_mode="HTML"
    )
    await callback.message.answer("–ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é:", reply_markup=get_user_main_menu_kb())
    await state.clear()
    await callback.answer()

